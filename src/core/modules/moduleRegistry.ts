export type ModuleStatus = 'enabled' | 'disabled' | 'preview' | 'locked';

export type ModuleRiskLevel = 'low' | 'medium' | 'high' | 'safety-sensitive';

export type WorkspaceKey =
    | 'restaurant'
    | 'retail'
    | 'pharmacy'
    | 'supermarket'
    | 'school'
    | 'services'
    | 'shared';

export type ModuleCategory =
    | 'dashboard'
    | 'commerce'
    | 'operations'
    | 'restaurant'
    | 'inventory'
    | 'crm'
    | 'documents'
    | 'team'
    | 'reports'
    | 'settings'
    | 'education'
    | 'future';

export interface Q360ModuleDefinition {
    moduleKey: string;
    workspaceKey: WorkspaceKey;
    label: string;
    description: string;
    status: ModuleStatus;
    category: ModuleCategory;
    isShared: boolean;
    riskLevel: ModuleRiskLevel;
    routePath?: string;
}

