export const getQ360ModulesByWorkspace = (workspaceKey: WorkspaceKey) =>
    q360ModuleRegistry.filter(module => module.workspaceKey === workspaceKey);

export const getQ360ModuleDefinition = (workspaceKey: WorkspaceKey, moduleKey: string) =>
    q360ModuleRegistry.find(module => module.workspaceKey === workspaceKey && module.moduleKey === moduleKey);

export const getQ360ModulesByStatus = (status: ModuleStatus) =>
    q360ModuleRegistry.filter(module => module.status === status);
